#!/bin/bash

## This file should be called from the directory above.

if [ ! -f "./${AUTOGENERATED_FILE_PREFIX}_config.bash" ] ; then
    echo "Cannot find ${AUTOGENERATED_FILE_PREFIX}_config.bash in current directory."
    echo "Maybe you didn't run setup?"
    exit 1
fi
source ./settings.bash 
source ./etc/functions.bash
source ./${AUTOGENERATED_FILE_PREFIX}_config.bash

if [ "${WORK_PATH}zzz" == "zzz" ] ; then
	print_error_and_exit "The work path is undefined."
fi
OUTFILE="${WORK_PATH}/VERSIONS.bash"

echo "#!/bin/bash
# Versions information updated on:  $(date)
#" > "${OUTFILE}"

# Checking that GEMS exists
if ! cd "${GEMS_BASE_PATH}" ; then
    print_error_and_exit "Could not cd to gems directory: ${GEMS_BASE_PATH}"
fi
export GEMSHOME="${GEMS_BASE_PATH}"

# Function that generates the info for each repo
# If a repo has a versions.txt file that it generates, like gmml2 does, you get that extra info.
generateRepoInfo () {
    repoPath=$1
    repoName=$2
    echo """

${repoName}_GIT_BRANCH=\"$(git -C "$repoPath" rev-parse --abbrev-ref HEAD)\"
${repoName}_GIT_COMMIT_HASH=\"$(git -C "$repoPath" rev-parse HEAD)\""""
    if [ -f "$repoPath"/version.txt ]; then
        echo "${repoName}_VERSION=\"$(cat "$repoPath"/version.txt)\""
    fi

}

# Set Git and version info for each repo. To add a new repo you just add a line here, nothing else.
{
generateRepoInfo "${GEMSHOME}" GEMS
generateRepoInfo "${GEMSHOME}"/gmml GMML
generateRepoInfo "${GEMSHOME}"/gmml2 GMML2
generateRepoInfo "${GEMSHOME}"/External/MD_Utils MD_Utils
} >> "${OUTFILE}"
echo "Successfully finished $0. Info should be in $OUTFILE"

